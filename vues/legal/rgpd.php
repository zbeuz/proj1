                <h3>5.6 Intégrité et confidentialité</h3>
                <p>Nous protégeons vos données par des mesures techniques et organisationnelles :</p>
                <ul>
                    <li>Chiffrement des mots de passe</li>
                    <li>Connexions sécurisées HTTPS</li>
                    <li>Contrôle d'accès strict</li>
                    <li>Formation du personnel</li>
                    <li>Audits de sécurité réguliers</li>
                </ul>

                <h3>5.7 Responsabilité</h3>
                <p>L'AFORP peut démontrer le respect du RGPD par :</p>
                <ul>
                    <li>Documentation des traitements</li>
                    <li>Analyses d'impact (AIPD)</li>
                    <li>Procédures de gestion des incidents</li>
                    <li>Formation continue du personnel</li>
                </ul>
            </div>

