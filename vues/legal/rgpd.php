                <h2>8. Sanctions du RGPD</h2>
                <p>Le RGPD prévoit des sanctions importantes en cas de non-respect :</p>
                
                <h3>8.1 Amendes administratives</h3>
                <ul>
                    <li>Jusqu'à <strong>10 millions d'euros</strong> ou 2% du chiffre d'affaires mondial</li>
                    <li>Jusqu'à <strong>20 millions d'euros</strong> ou 4% du chiffre d'affaires mondial</li>
                </ul>

                <h3>8.2 Autres sanctions</h3>
                <ul>
                    <li>Avertissement</li>
                    <li>Mise en demeure</li>
                    <li>Limitation ou interdiction de traitement</li>
                    <li>Suspension des flux de données</li>
                </ul>
