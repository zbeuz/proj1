                <h3>2.2 Données sensibles</h3>
                <p>Certaines données nécessitent une protection renforcée :</p>
                <ul>
                    <li>Données de santé</li>
                    <li>Opinions politiques, religieuses</li>
                    <li>Orientation sexuelle</li>
                    <li>Données biométriques</li>
                </ul>
                
                <div class="info-box">
                    <h4>À l'AFORP</h4>
                    <p>Nous ne collectons <strong>aucune donnée sensible</strong> dans le cadre de notre plateforme pédagogique.</p>
                </div>
            </div>

            <div class="legal-section">
                <h2>3. Vos droits selon le RGPD</h2>
                <p>Le RGPD vous confère 8 droits fondamentaux concernant vos données personnelles :</p>

                <h3>3.1 Droit à l'information</h3>
                <p><strong>Définition :</strong> Être informé de la collecte et de l'utilisation de vos données.</p>
                <p><strong>À l'AFORP :</strong> Notre <a href="politique-confidentialite.php">Politique de Confidentialité</a> détaille tous les traitements.</p>

                <h3>3.2 Droit d'accès</h3>
                <p><strong>Définition :</strong> Accéder à toutes vos données et aux informations sur leur traitement.</p>
                <p><strong>À l'AFORP :</strong> Contactez notre DPO pour obtenir une copie de vos données.</p>

                <h3>3.3 Droit de rectification</h3>
                <p><strong>Définition :</strong> Corriger des données inexactes ou incomplètes.</p>
                <p><strong>À l'AFORP :</strong> Signalez toute erreur via votre espace personnel ou au DPO.</p>

                <h3>3.4 Droit à l'effacement ("droit à l'oubli")</h3>
                <p><strong>Définition :</strong> Demander la suppression de vos données dans certaines conditions.</p>
                <p><strong>À l'AFORP :</strong> Possible après la fin de formation, sauf obligations légales d'archivage.</p>

                <h3>3.5 Droit à la limitation du traitement</h3>
                <p><strong>Définition :</strong> Demander la suspension temporaire du traitement de vos données.</p>
                <p><strong>À l'AFORP :</strong> En cas de contestation ou d'opposition au traitement.</p>

                <h3>3.6 Droit à la portabilité</h3>
                <p><strong>Définition :</strong> Récupérer vos données dans un format structuré et lisible.</p>
                <p><strong>À l'AFORP :</strong> Export possible de vos travaux et données pédagogiques.</p>

                <h3>3.7 Droit d'opposition</h3>
                <p><strong>Définition :</strong> S'opposer au traitement de vos données pour motif légitime.</p>
                <p><strong>À l'AFORP :</strong> Limité car nécessaire à la formation, sauf cas particuliers.</p>

                <h3>3.8 Droits relatifs aux décisions automatisées</h3>
                <p><strong>Définition :</strong> Ne pas faire l'objet de décisions entièrement automatisées.</p>
                <p><strong>À l'AFORP :</strong> Aucune décision automatisée n'est prise sur la plateforme.</p>
            </div>

