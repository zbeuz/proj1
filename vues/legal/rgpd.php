            <h1 class="legal-title">RGPD - Protection des Données</h1>
            <p class="legal-subtitle">Comprendre le Règlement Général sur la Protection des Données et vos droits</p>

            <div class="legal-section">
                <h2>1. Qu'est-ce que le RGPD ?</h2>
                <p>Le <strong>Règlement Général sur la Protection des Données (RGPD)</strong> est un texte réglementaire européen qui encadre le traitement des données personnelles sur le territoire de l'Union européenne.</p>
                
                <div class="info-box">
                    <h4>Entrée en vigueur</h4>
                    <p>Le RGPD est entré en application le <strong>25 mai 2018</strong> dans tous les pays de l'Union européenne, y compris la France.</p>
                </div>

                <h3>1.1 Objectifs du RGPD</h3>
                <ul>
                    <li>Renforcer le contrôle des citoyens sur leurs données personnelles</li>
                    <li>Harmoniser la réglementation européenne</li>
                    <li>Responsabiliser les entreprises et organismes</li>
                    <li>Moderniser le cadre juridique face aux évolutions technologiques</li>
                </ul>
            </div>

            <div class="legal-section">
                <h2>2. Données personnelles : définition</h2>
                <p>Selon le RGPD, une <strong>donnée personnelle</strong> est toute information se rapportant à une personne physique identifiée ou identifiable.</p>

                <h3>2.1 Exemples de données personnelles</h3>
                <table class="legal-table">
                    <thead>
