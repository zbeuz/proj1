            <div class="legal-section">
                <h2>5. Principes du RGPD appliqués à l'AFORP</h2>
                <h3>5.1 Licéité, loyauté, transparence</h3>
                <p>Tous nos traitements ont une base légale claire (contrat de formation, obligation légale, intérêt légitime).</p>

                <h3>5.2 Limitation des finalités</h3>
                <p>Vos données sont utilisées uniquement pour les finalités pédagogiques déclarées.</p>

                <h3>5.3 Minimisation des données</h3>
                <p>Nous ne collectons que les données strictement nécessaires à la formation.</p>

                <h3>5.4 Exactitude</h3>
                <p>Nous maintenons vos données à jour et vous permettons de les corriger.</p>

                <h3>5.5 Limitation de la conservation</h3>
                <p>Vos données sont conservées uniquement le temps nécessaire :</p>
