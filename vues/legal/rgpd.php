                <h2>6. Violations de données</h2>
                <h3>6.1 Notre engagement</h3>
                <p>En cas de violation de données personnelles :</p>
                <ul>
                    <li>Notification à la CNIL sous 72h</li>
                    <li>Information des personnes concernées si risque élevé</li>
                    <li>Mesures correctives immédiates</li>
                    <li>Documentation de l'incident</li>
                </ul>

                <h3>6.2 Prévention</h3>
                <p>Nous mettons en place des mesures préventives :</p>
                <ul>
                    <li>Surveillance continue de la sécurité</li>
                    <li>Tests de pénétration réguliers</li>
                    <li>Sauvegardes chiffrées</li>
                    <li>Plans de continuité d'activité</li>
                </ul>
            </div>

